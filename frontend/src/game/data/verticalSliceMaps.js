export function tileToWorld(tile) {
  return tile * TILE_SIZE + TILE_SIZE / 2
}

function rect(x, y, w, h) {
  return Array.from({ length: w * h }, (_, index) => ({ x: x + (index % w), y: y + Math.floor(index / w) }))
}
